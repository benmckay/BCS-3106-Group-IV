from django.contrib import admin
from django.urls import path, include, re_path
from django.conf import settings
from django.conf.urls.static import static
from rest_framework import permissions
from drf_yasg.views import get_schema_view
from drf_yasg import openapi
from construction.views import favicon

# Swagger/OpenAPI documentation setup
schema_view = get_schema_view(
    openapi.Info(
        title="Bidii Quality Builders API",
        default_version='v1',
        description="""
        API for Bidii Quality Builders Construction Management System
        
        This API provides endpoints for managing:
        - Customers and their contact information
        - Workers and their skills
        - Estimates for construction work
        - Job scheduling and management
        - Material ordering and tracking
        - Invoicing and payment processing
        - Dashboard analytics and reports
        """,
        terms_of_service="https://www.example.com/terms/",
        contact=openapi.Contact(email="info@bidiibuilders.com"),
        license=openapi.License(name="BSD License"),
    ),
    public=True,
    permission_classes=(permissions.AllowAny,),
)

urlpatterns = [
    path('admin/', admin.site.urls),
    path('api/', include('construction.urls')),
    path('favicon.ico', favicon, name='favicon'),
    
    # Swagger documentation
    re_path(r'^swagger(?P<format>\.json|\.yaml)$', schema_view.without_ui(cache_timeout=0), name='schema-json'),
    path('swagger/', schema_view.with_ui('swagger', cache_timeout=0), name='schema-swagger-ui'),
    path('redoc/', schema_view.with_ui('redoc', cache_timeout=0), name='schema-redoc'),
    path('', schema_view.with_ui('swagger', cache_timeout=0), name='api-root'),
]

# Serve media files in development
if settings.DEBUG:
    urlpatterns += static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)
    urlpatterns += static(settings.STATIC_URL, document_root=settings.STATIC_ROOT)
